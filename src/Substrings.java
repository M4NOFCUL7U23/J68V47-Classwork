public class Substrings {
    public static void main(String[] args) {
        //Substrings
        String message = "Hello world";
        System.out.println(message.substring(0,2)); //Displays "e"
        System.out.println(message.substring(3,8)); //displays "o wo"
        System.out.println(message.substring(0,2)); //displays "H"

    }
}
